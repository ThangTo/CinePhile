/**
 * Get comments for a specific movie
 * @param {string|number} movieId - Movie ID
 * @returns {Array} Array of comments
 */
export const getMovieComments = (movieId) => {
  return mockComments[movieId] || mockComments.default;
};

/**
 * Get movie detail by ID
 * @param {string|number} id - Movie ID
 * @returns {Object|null} Movie detail object or null if not found
 */
export const getMovieDetail = (id) => {
  // First check in mockMovieDetails
  if (mockMovieDetails[id]) {
    return {
      ...mockMovieDetails[id],
      comments: getMovieComments(id),
    };
  }

  // Then search in mockTop10Movies
  const top10Movie = mockTop10Movies.find((m) => m.id === Number(id) || m.id === id);
  if (top10Movie) {
    return {
      ...top10Movie,
      synopsis: top10Movie.description || top10Movie.overview,
      cast: [],
      episodes: Array.from({ length: top10Movie.totalEpisodes || 10 }, (_, i) => ({
        id: i + 1,
        title: `Táº­p ${i + 1}`,
        duration: "45m",
      })),
      comments: getMovieComments(top10Movie.id),
    };
  }

  // Then search in mockSectionMovies
  const allSectionMovies = [
    ...(mockSectionMovies.trending || []),
    ...(mockSectionMovies.newReleases || []),
  ];
  const sectionMovie = allSectionMovies.find((m) => m.id === Number(id) || m.id === id);
  if (sectionMovie) {
    return {
      ...sectionMovie,
      synopsis: sectionMovie.description || sectionMovie.overview,
      cast: [],
      episodes: Array.from({ length: sectionMovie.totalEpisodes || 10 }, (_, i) => ({
        id: i + 1,
        title: `Táº­p ${i + 1}`,
        duration: "45m",
      })),
      comments: getMovieComments(sectionMovie.id),
    };
  }

  return null;
};
