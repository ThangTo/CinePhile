/**
 * Format episode info for display
 * @param {Object} movie - Movie object
 * @returns {string} Formatted episode string
 */
export const formatEpisodeInfo = (movie) => {
  if (movie.currentEpisode && movie.totalEpisodes) {
    return `${movie.ageRating} • Phần ${movie.season || 1} • Tập ${movie.currentEpisode}`;
  }
  return movie.episode || "Đang cập nhật";
};

/**
 * Format views count
 * @param {number} views - Number of views
 * @returns {string} Formatted views string
 */
export const formatViews = (views) => {
  if (views >= 1000000) {
    return `${(views / 1000000).toFixed(1)}M lượt xem`;
  }
  if (views >= 1000) {
    return `${(views / 1000).toFixed(1)}K lượt xem`;
  }
  return `${views} lượt xem`;
};

